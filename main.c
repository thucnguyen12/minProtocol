uint8_t getChar(USART_TypeDef * uart)
 {
    uint8_t t;
    while (!(uart->SR & (1 << 5))){}  //wait until rxen set
    t = uart ->DR;
    return t;        
 }
     
void putChar(USART_TypeDef * uart , uint8_t c)
{
    //HAL_Delay (1);
    uart ->DR = c;
    while (!(uart ->SR & (uint32_t )(1 << 6)));  // wait until tc set
}

